package com.modelo;

public class Tematica {

	private String nombreTematica;
	
	private String detalle1;
	private String enlace1;
	private String nombre1;
	
	private String detalle2;
	private String enlace2;
	private String nombre2;
	
	private String detalle3;
	private String enlace3;
	private String nombre3;
	
	
	public Tematica(String nombreTematica, String detalle1, String enlace1, String nombre1, String detalle2,
			String enlace2, String nombre2, String detalle3, String enlace3, String nombre3) {
		super();
		this.nombreTematica = nombreTematica;
		this.detalle1 = detalle1;
		this.enlace1 = enlace1;
		this.nombre1 = nombre1;
		this.detalle2 = detalle2;
		this.enlace2 = enlace2;
		this.nombre2 = nombre2;
		this.detalle3 = detalle3;
		this.enlace3 = enlace3;
		this.nombre3 = nombre3;
	}


	public String getNombreTematica() {
		return nombreTematica;
	}


	public void setNombreTematica(String nombreTematica) {
		this.nombreTematica = nombreTematica;
	}


	public String getDetalle1() {
		return detalle1;
	}


	public void setDetalle1(String detalle1) {
		this.detalle1 = detalle1;
	}


	public String getEnlace1() {
		return enlace1;
	}


	public void setEnlace1(String enlace1) {
		this.enlace1 = enlace1;
	}


	public String getNombre1() {
		return nombre1;
	}


	public void setNombre1(String nombre1) {
		this.nombre1 = nombre1;
	}


	public String getDetalle2() {
		return detalle2;
	}


	public void setDetalle2(String detalle2) {
		this.detalle2 = detalle2;
	}


	public String getEnlace2() {
		return enlace2;
	}


	public void setEnlace2(String enlace2) {
		this.enlace2 = enlace2;
	}


	public String getNombre2() {
		return nombre2;
	}


	public void setNombre2(String nombre2) {
		this.nombre2 = nombre2;
	}


	public String getDetalle3() {
		return detalle3;
	}


	public void setDetalle3(String detalle3) {
		this.detalle3 = detalle3;
	}


	public String getEnlace3() {
		return enlace3;
	}


	public void setEnlace3(String enlace3) {
		this.enlace3 = enlace3;
	}


	public String getNombre3() {
		return nombre3;
	}


	public void setNombre3(String nombre3) {
		this.nombre3 = nombre3;
	}
	
	
	
	
	
}
